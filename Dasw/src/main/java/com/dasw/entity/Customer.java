package com.dasw.entity;

public class Customer {
    private Integer customerId;

    private Integer userId;

    private String customerType;

    private String customerName;

    private String customerTel;

    private String customerCompanyName;

    private String customerCompanyAddress;

    private String customerCompanyTel;

    private String customerReceiverName;

    private String customerReceiverTel;

    private String customerReceiverAddress;

    public Integer getCustomerId() {
        return customerId;
    }

    public void setCustomerId(Integer customerId) {
        this.customerId = customerId;
    }

    public Integer getUserId() {
        return userId;
    }

    public void setUserId(Integer userId) {
        this.userId = userId;
    }

    public String getCustomerType() {
        return customerType;
    }

    public void setCustomerType(String customerType) {
        this.customerType = customerType;
    }

    public String getCustomerName() {
        return customerName;
    }

    public void setCustomerName(String customerName) {
        this.customerName = customerName;
    }

    public String getCustomerTel() {
        return customerTel;
    }

    public void setCustomerTel(String customerTel) {
        this.customerTel = customerTel;
    }

    public String getCustomerCompanyName() {
        return customerCompanyName;
    }

    public void setCustomerCompanyName(String customerCompanyName) {
        this.customerCompanyName = customerCompanyName;
    }

    public String getCustomerCompanyAddress() {
        return customerCompanyAddress;
    }

    public void setCustomerCompanyAddress(String customerCompanyAddress) {
        this.customerCompanyAddress = customerCompanyAddress;
    }

    public String getCustomerCompanyTel() {
        return customerCompanyTel;
    }

    public void setCustomerCompanyTel(String customerCompanyTel) {
        this.customerCompanyTel = customerCompanyTel;
    }

    public String getCustomerReceiverName() {
        return customerReceiverName;
    }

    public void setCustomerReceiverName(String customerReceiverName) {
        this.customerReceiverName = customerReceiverName;
    }

    public String getCustomerReceiverTel() {
        return customerReceiverTel;
    }

    public void setCustomerReceiverTel(String customerReceiverTel) {
        this.customerReceiverTel = customerReceiverTel;
    }

    public String getCustomerReceiverAddress() {
        return customerReceiverAddress;
    }

    public void setCustomerReceiverAddress(String customerReceiverAddress) {
        this.customerReceiverAddress = customerReceiverAddress;
    }
}